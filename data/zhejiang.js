    // 获取所有建筑
    getAllBuildings() {
        return this.buildings.map(building => ({
            ...building,
            province: this.provinceName,
            provinceId: this.provinceId
        }));
    },

    // 根据区县获取建筑
    getBuildingsByDistrict(districtId) {
        return this.buildings
            .filter(b => b.district === districtId)
            .map(building => ({
                ...building,
                province: this.provinceName,
                provinceId: this.provinceId
            }));
    },

    // 根据名称获取建筑
    getBuildingByName(name) {
        let building = this.buildings.find(b => b.name === name);
        if (!building) {
            building = this.buildings.find(b => {
                const fullPath = `${this.provinceName}${b.districtName}${b.name}`;
                return fullPath === name;
            });
        }
        if (building) {
            return { ...building, province: this.provinceName, provinceId: this.provinceId };
        }
        return null;
    },

    // 获取所有区县
    getAllDistricts() {
        return Object.entries(this.districts).map(([id, data]) => ({ id, ...data }));
    },

    // 根据标签获取建筑
    getBuildingsByTag(tag) {
        return this.buildings
            .filter(b => b.tags.includes(tag))
            .map(building => ({ ...building, province: this.provinceName, provinceId: this.provinceId }));
    },

    // 搜索建筑
    searchBuildings(query) {
        const lowerQuery = query.toLowerCase();
        return this.buildings
            .filter(b =>
                b.name.toLowerCase().includes(lowerQuery) ||
                b.location.toLowerCase().includes(lowerQuery) ||
                b.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
            )
            .map(building => ({ ...building, province: this.provinceName, provinceId: this.provinceId }));
    }
